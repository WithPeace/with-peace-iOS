enum ReportMessage: String {
    case duplicate = "DUPLICATE"
    case advertisement = "ADVERTISEMENT"
    case inappropriate = "INAPPROPRIATE"
    case profanity = "PROFANITY"
    case obscenity = "OBSCENITY"
}

// DUPLICATE (중복 / 도배성 게시글이에요)
// ADVERTISEMENT (광고 / 홍보 글이에요)
// INAPPROPRIATE (게시판 성격에 부적절한 글이에요)
// PROFANITY (욕설 / 혐오 표현이 사용된 글이에요)
// OBSCENITY (음란성 / 선정적인 글이에요)

